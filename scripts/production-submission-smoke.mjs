#!/usr/bin/env node

const baseUrl = (process.env.FOR_AI_BASE_URL || "https://for-ai-e4mm.vercel.app").replace(/\/$/, "");
const adminSecret = process.env.ADMIN_SECRET || "";
const runId = new Date().toISOString().replace(/[-:.]/g, "");

const requiredDeploymentEnv = [
  "NEXT_PUBLIC_SUPABASE_URL",
  "NEXT_PUBLIC_SUPABASE_ANON_KEY",
  "SUPABASE_SERVICE_ROLE_KEY",
  "CONTRIBUTOR_SALT",
  "ADMIN_SECRET",
];

const forbiddenErrors = [
  "DB not configured",
  "Server configuration error",
  "unauthorized",
  "SUPABASE_SERVICE_ROLE_KEY not configured",
];

function localEnvStatus() {
  return Object.fromEntries(requiredDeploymentEnv.map((name) => [name, Boolean(process.env[name])]));
}

async function requestJson(name, path, init = {}) {
  try {
    const response = await fetch(`${baseUrl}${path}`, {
      ...init,
      headers: {
        "content-type": "application/json",
        ...(init.headers || {}),
      },
    });
    const text = await response.text();
    let json = null;
    try {
      json = text ? JSON.parse(text) : null;
    } catch {
      // Keep text for diagnostics.
    }
    const bodyText = json ? JSON.stringify(json) : text;
    const matchedForbidden = forbiddenErrors.find((message) => bodyText.includes(message));
    const ok = response.ok && !matchedForbidden;
    return { name, path, status: response.status, ok, bodyText, json, matchedForbidden };
  } catch (error) {
    return {
      name,
      path,
      status: "network-error",
      ok: false,
      bodyText: error instanceof Error ? error.message : String(error),
      json: null,
      matchedForbidden: null,
    };
  }
}

function printResult(result) {
  const marker = result.ok ? "✅" : "❌";
  console.log(`${marker} ${result.name}: HTTP ${result.status}`);
  if (!result.ok) {
    if (result.matchedForbidden) console.log(`   forbidden error observed: ${result.matchedForbidden}`);
    console.log(`   body: ${result.bodyText.slice(0, 500)}`);
  }
}

async function main() {
  console.log(`For-Ai production submission smoke test`);
  console.log(`Base URL: ${baseUrl}`);
  console.log(`Run ID: ${runId}`);
  console.log(`Local shell env presence (does not prove Vercel settings): ${JSON.stringify(localEnvStatus())}`);

  const results = [];

  results.push(await requestJson("/community 글쓰기", "/api/posts", {
    method: "POST",
    body: JSON.stringify({
      author_type: "user",
      author_name: "For-Ai Smoke Test",
      content: `운영 제출 테스트 ${runId} - community`,
    }),
  }));

  results.push(await requestJson("/suggest-topic 토픽 제안", "/api/suggest-topic", {
    method: "POST",
    body: JSON.stringify({
      question: `운영 제출 테스트 ${runId}`,
      category: "technology",
      suggested_slug: `smoke-test-${runId.toLowerCase()}`,
      reason: "배포 환경 제출 플로우 검증",
      source_url: "https://example.com/for-ai-smoke-test",
      ai_context: "Smoke test generated by For-Ai production-submission-smoke script.",
    }),
  }));

  results.push(await requestJson("/report/seoul-metro-base-fare 정정 요청", "/api/report/seoul-metro-base-fare", {
    method: "POST",
    body: JSON.stringify({
      report_type: "correction",
      message: `운영 제출 테스트 ${runId} - correction request`,
    }),
  }));

  results.push(await requestJson("/hallucination/seoul-metro-base-fare AI 오답 신고", "/api/hallucination/seoul-metro-base-fare", {
    method: "POST",
    body: JSON.stringify({
      ai_service: "For-Ai Smoke Test",
      prompt: "운영 제출 테스트",
      ai_answer: "테스트 답변",
      expected_correction: `운영 제출 플로우 검증 ${runId}`,
    }),
  }));

  if (!adminSecret) {
    results.push({
      name: "/admin/posts 관리자 상태 변경",
      path: "/api/admin/posts",
      status: "skipped",
      ok: false,
      bodyText: "ADMIN_SECRET is not set in the local shell; cannot authenticate admin smoke test.",
      matchedForbidden: "unauthorized",
    });
  } else {
    const create = await requestJson("/admin/posts 관리자 글 생성", "/api/admin/posts", {
      method: "POST",
      headers: { "x-admin-secret": adminSecret },
      body: JSON.stringify({
        author_type: "admin",
        author_name: "For-Ai Smoke Test",
        content: `운영 관리자 상태 변경 테스트 ${runId}`,
      }),
    });
    results.push(create);

    const postId = create.json?.id;
    for (const status of ["hidden", "spam", "deleted", "published"]) {
      results.push(await requestJson(`/admin/posts 상태 변경: ${status}`, "/api/admin/posts", {
        method: "PATCH",
        headers: { "x-admin-secret": adminSecret },
        body: JSON.stringify({ id: postId, status }),
      }));
    }
  }

  console.log("\nResults:");
  results.forEach(printResult);

  const failed = results.filter((result) => !result.ok);
  if (failed.length) {
    console.error(`\n${failed.length} smoke check(s) failed.`);
    process.exitCode = 1;
  } else {
    console.log("\nAll smoke checks passed without forbidden configuration/auth errors.");
  }
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
